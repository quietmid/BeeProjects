#include "libft.h"

void    *ft_memchr(const void *s, int c, size_t n)
{
    size_t  i;
    char    *str;

    str = ((char *)s);
    i = 0;
    while (str[i] != '\0' && i < n)
    {
        if (str[i] == c)
        {
            return (&str[i]);
        }
        i++;
    }
    return (NULL);
}
