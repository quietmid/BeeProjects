#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst)
		return ;
	if (!del)
		return ;
	if (!(lst->content))
		return ;
	del(lst->content);
	free(lst);
	lst = NULL;
}
