#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*str;

	str = (char *)s;
	i = 0;
	while (str[i] != '\0' && str[i] != c)
		i++;
	if (str[i] == c || (c == '\0' && str[i] == '\0'))
	{
		return (&str[i]);
	}
	return (NULL);
}
